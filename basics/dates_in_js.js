let myDate = new Date();
console.log(myDate);
                                 //Output
console.log(myDate.toString());  //Wed Jan 29 2025 08:47:20 GMT+0000 (Coordinated Universal Time)

console.log(myDate.toDateString()); //Wed Jan 29 2025

console.log(myDate.toLocaleDateString()); //1/29/2025

console.log(myDate.toLocaleString()); //1/29/2025, 8:49:06 AM

console.log(typeof myDate); //Object

console.log(myDate.getMonth());
console.log(myDate.getDate());
